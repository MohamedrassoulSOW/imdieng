        <h2 class="sub-heading1">Confier la présidence de l’Assemblée Nationale à Ousmane Sonko pourrait être un faux pas stratégique.</h2>
        <p class="post-text1">Mettre Ousmane Sonko à la présidence de l’Assemblée nationale serait une erreur stratégique majeure, tant sur le plan politique que dans la mise en œuvre de la vision 2050. Cette fonction, bien que prestigieuse, réduirait considérablement son rôle dans le duo qu’il forme avec le Président Bassirou Diomaye Faye, un duo qui incarne la stabilité et le changement tant attendu par les Sénégalais. La présidence de l’Assemblée nationale impose un rôle strictement législatif. Avec le principe de séparation des pouvoirs, Ousmane Sonko serait contraint de se tenir à l’écart des décisions exécutives. Cela signifie qu’il ne pourrait ni orienter les ministres ni garantir une application stricte des projets gouvernementaux. En tant que Président de l’Assemblée, tout entretien fréquent avec le Président de la République pourrait être perçu comme une ingérence qui fragilise la gouvernance.</p>
        <p class="post-text1">En sa qualité de Premier ministre, Ousmane Sonko est une véritable armure pour le gouvernement. Il agit comme le garant de la cohérence entre les différentes branches de l’exécutif et veille à la discipline au sein des ministères et directions générales. Sa position permet un contrôle direct et rigoureux, indispensable pour mener à bien un projet aussi ambitieux que la vision 2050. Reléguer ce rôle à un autre pour placer Sonko à l’Assemblée Nationale reviendrait à priver le gouvernement de son bouclier le plus efficace. Il passerait de la co-gestion du pouvoir à une posture de contre-pouvoir. Ce changement transformerait le duo en une dualité et créerait un risque de friction institutionnelle. Les adversaires du régime, les hommes du système que nous venons d’enterrer, conscients de la puissance de ce binôme, n’ont de cesse de chercher à les opposer subtilement.
        </p>
        <p class="post-text1">Ousmane Sonko est l’âme du projet gouvernemental et son incarnation. Sa vision, son dynamisme et son charisme sont les moteurs qui mobilisent les sénégalais autour de l’idée de rupture systémique et de souveraineté. En faire un simple acteur législatif reviendrait à l’éloigner de la mise en œuvre concrète de cette vision. Le projet repose sur lui et sa capacité à inspirer et à coordonner les efforts à tous les niveaux. Le PM peut et doit coordonner les ministères, superviser les grands projets et renforcer la synergie entre l’exécutif et les populations. Ce rôle est bien plus central et impactant que celui de Président de l’Assemblée, limité aux fonctions de législation et de contrôle parlementaire. La réussite de la vision 2050 repose sur le duo complémentaire Sonko/Diomaye. Leur rôle est de rester au cœur des décisions, de protéger le gouvernement et de garantir que le Sénégal reste sur le chemin de la rupture et du progrès. Le moment est venu de resserrer les rangs et d’éviter les pièges politiques tendus par les adversaires du changement. C’est l’heure du Jubbanti.
        </p>
