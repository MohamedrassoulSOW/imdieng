        <h2 class="sub-heading1">Alerte! Alerte! Alerte ! Tiktok va nous détruire.</h2>
        <p class="post-text1">Mame Ndiaye Savon, Adamo, Awoush Kitty… des noms qui attirent des milliers de jeunes à travers leurs « lives » sur TikTok, où le nombre de vues et la vulgarité semblent devenir la seule boussole de leur engagement. Ces personnes, dépourvues de valeurs, exercent une influence insidieuse sur notre jeunesse et contribuent silencieusement à la dégradation des normes sociales. L'usage des réseaux sociaux, notamment TikTok, a explosé ces dernières années, entraînant des effets profonds sur les sociétés à travers le monde. Au Sénégal, ce phénomène est particulièrement préoccupant, et il est urgent que les autorités prennent des mesures pour encadrer l'utilisation de cette plateforme, notamment par les jeunes.</p>
        <p class="post-text1">Alerte ! Nous sommes en face de risques d'une société déconnectée de la réalité. Les réseaux sociaux, en particulier TikTok, sont devenus de véritables refuges pour une grande partie de la jeunesse sénégalaise. Toutefois, ce refuge est souvent illusoire, car ces plateformes sont essentiellement basées sur la création et la consommation de contenus éphémères, qui valorisent la superficialité. Les jeunes passent des heures à scroller, à commenter, à partager des vidéos souvent futiles, sans aucune réelle valeur éducative. Pire encore, ces contenus encouragent parfois des comportements inappropriés, mettant en avant la vie privée des individus ou promouvant des attitudes déviantes. Ce phénomène ne fait que renforcer une culture de la médiocrité, où l’apparence et les actions instantanées priment sur la réflexion et l’effort soutenu.
        </p>
        <p class="post-text1">Alerte! J’alerte encore. L’impact sur la jeunesse est d’autant plus préoccupant qu’elle est la principale cible de ces plateformes. Les jeunes, encore en phase de construction identitaire, se retrouvent submergés par des contenus qui déforment leur perception de la réalité. Les notions de travail, de persévérance et de mérite sont remplacées par la recherche du buzz et du sensationnel. Les conséquences sont désastreuses : perte de repères, baisse de l’estime de soi, troubles alimentaires et parfois même des tendances à l’automutilation, comme cela a été observé dans certaines études. TikTok, en tant que plateforme, n’est pas neutre dans ce processus. L’algorithme qui régit les contenus proposés aux utilisateurs est conçu pour maximiser l’engagement, souvent au détriment du bien-être mental des jeunes.
        </p>
        <p class="post-text1">Alerte. J’alerte et je propose. Un système régulatoire est urgent. Face à ces constats, la responsabilité de l’État est engagée. Le précédent régime a montré qu’il pouvait suspendre TikTok pour des raisons politiques, ce qui prouve que des mesures peuvent être prises pour encadrer l’utilisation de ce réseau social. Pour convoyer Ousmane Sanko au tribunal, tiktok était suspendu en un rien de temps. C’est donc possible. La nouvelle administration doit aller plus loin en instaurant un cadre régulatoire strict pour protéger la jeunesse. Des exemples concrets existent déjà. En Chine, la version locale de TikTok, Douyin, est soumise à des restrictions sévères : limitation du temps d’écran, contrôle parental  et un contenu adapté pour les mineurs. Ces mesures sont mises en place pour prévenir les effets négatifs de l’addiction aux réseaux sociaux.
        </p>
        <p class="post-text1">J’alerte. Il est une urgence urgente que le Sénégal s’inspire de telles initiatives pour réguler l’usage de TikTok et autres plateformes similaires. L’État pourrait envisager des restrictions temporelles pour les utilisateurs mineurs, renforcer l’éducation numérique dans les écoles, et encourager la création de contenus locaux à valeur ajoutée, qui valorisent les connaissances et les compétences pratiques. Le but n’est pas et ne doit pas être de censurer, mais de créer un environnement numérique sain, où les jeunes peuvent s’épanouir sans être exposés à des contenus potentiellement dangereux.
        </p>
        <p class="post-text1">C’est une responsabilité collective. J’alerte. Parents, autorités politiques, étudiants, enseignants, nous sommes tous concernés. Il faut que les autorités prennent la pleine mesure des dangers posés par l’utilisation non régulée des réseaux sociaux. Les conséquences d'une inaction pourraient être catastrophiques pour l’avenir du pays. Une société où les jeunes sont déconnectés de la réalité, immergés dans une culture de l’instantanéité et de la superficialité est une société vouée à la décadence. Irréversible. Ce n’est pas seulement la jeunesse qui en souffrira, mais l’ensemble de la nation. Il est donc de notre devoir collectif d’agir pour sauvegarder les valeurs qui fondent notre société. Une régulation stricte soutenue par une politique de l’éducation numérique, peut garantir un avenir où les réseaux sociaux seront utilisés comme des outils d’émancipation et non de déchéance. C’est possible, il faut agit. J’ai alerté. Je rêve d’un Sénégal meilleur, conduit par des valeurs.</p>
